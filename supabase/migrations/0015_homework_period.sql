-- ============================================================
-- 0015 — 숙제 '제출 기간' 도입
-- 단일 마감일 개념 대신 [시작일 ~ 종료일] 기간으로 운영한다.
--   · start_date : 제출 시작일(선택, NULL이면 즉시 시작)
--   · due_date   : 기존 컬럼을 '제출 종료일'로 재사용
-- 상태:
--   오늘 < start_date            → 시작 전(예정)
--   start_date ≤ 오늘 ≤ due_date → 진행중(미제출 = 제출 전 대기)
--   오늘 > due_date              → 종료(미제출 = 미제출자, 문자 발송 대상)
-- 0004 적용 이후 실행.
-- ============================================================

alter table homework
  add column if not exists start_date date;
